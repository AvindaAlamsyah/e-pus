                    <h4 class="mb-4">Standar Pelayanan</h4>
                    <p class="mb-5">1. Persyaratan layanan<br />

                        Semua masyarakat baik dalam maupun luar negeri dapat memperoleh layanan informasi standardisasi dengan persyaratan:<br />

                        Persyaratan umum bagi masyarakat, baik dalam maupun luar negeri, untuk mendapatkan Layanan Informasi SPK adalah sebaga berikut:<br />

                        Pengguna yang datang langsung ke LITe BSN wajib mengisi buku tamu;<br />
                        Pengguna layanan online wajib melakukan pendaftaran di website pemesanan standar online BSN;<br />
                        Pengguna bersedia melakukan pembayaran layanan dokumen standar melalui Sistem Informasi PNBP Online (SIMPONI);<br />
                        Pengguna dengan tarif Penerimaan Negara Bukan Pajak (PNBP) tertentu sebagaimana diatur dalam PP No. 40 Tahun 2018 wajib melengkapi berkas lampiran sesuai ketentuan.<br />


                        3. Sistem, mekanisme dan prosedur layanan<br />

                        Layanan Informasi SPK di LITe BSN menggunakan sistem layanan terbuka dan tertutup. Pada sistem terbuka pengguna dapat langsung mencari informasi/koleksi referensi Perpustakaan BSN secara langsung di jajaran rak. Sementara pada sistem tertutup, terutama layanan dokumen standar, pengguna dapat mengisi formulir terlebih dahulu dan menyerahkannya kepada petugas. Petugas akan mengambilkan/memproses dokumen standar sesuai permintaan pengguna. Pada layanan konsultasi, pengguna layanan yang datang langsung ke LITe BSN akan dilayani menggunakan sistem antrian.<br />

                        Layanan sirkulasi (peminjaman dan pengembalian) dokumen standar dan buku hanya diperuntukkan bagi pengguna internal BSN (pegawai dan staf). Sementara pengguna umum (eksternal) dapat memanfaatkan layanan baca di tempat dokumen standar dan buku. Layanan penyediaan dokumen standar, baik SNI maupun standar internasional dan konsultasi diperuntukkan bagi masyarakat secara umum.<br />

                        Dalam rangka kemudahan akses dan temu kembali informasi bagi pengguna, LITe BSN menyediakan alat akses, baik elektronik maupun tercetak. Koleksi referensi dan repositori dapat diakses melalui portal Perpustakaan BSN (perpustakaan.bsn.go.id), koleksi dokumen SNI dapat diakses melalui portal SISPK (sispk.bsn.go.id) dan koleksi standar internasional dapat diakses sesuai portal badan standar yang dibutuhkan.<br />

                        Mekanisme dan prosedur untuk masing-masing Layanan Informasi SPK di Perpustakaan BSN adalah sebagai berikut:<br />

                        #1 Layanan sirkulasi koleksi perpustakaan<br />

                        1. Layanan sirkulasi koleksi dokumen standar dan buku<br />

                        Pengguna mencari koleksi melalui alat akses koleksi;<br />
                        Pengguna mengisi formulir peminjaman koleksi;<br />
                        Pengguna menyerahkan formulir yang sudah diisi kepada petugas<br />
                        Petugas mengambilkan koleksi sesuai permintaan;<br />
                        Untuk koleksi referensi (buku), pengguna dapat langsung menelusur ke jajaran rak koleksi;<br />
                        Petugas memproses peminjaman melalui sistem dan/atau menyerahkan koleksi sesuai permintaan;<br />
                        Koleksi yang akan dikembalikan, dibawa dan diserahkan ke petugas untuk dicatat dalam sistem.<br />

                        2. Layanan baca di tempat dokumen standar dan buku<br />

                        Pengguna mencari koleksi melalui alat akses;<br />
                        Pengguna mengisi formulir permintaan dokumen standar/buku;<br />
                        Pengguna menyerahkan formulir yang sudah diisi kepada petugas;<br />
                        Petugas mengambilkan standar/buku sesuai permintaan;<br />
                        Petugas menyerahkan dokumen standar/buku yang diminta;<br />
                        Setelah selesai dibaca, pengguna mengembalikan dokumen standar/buku kepada petugas layanan.<br />


                        #2 Layanan konsultasi standardisasi dan penilaian kesesuaian<br />

                        Pengguna datang ke LITe BSN untuk berkonsultasi secara tatap muka;<br />
                        Pengguna akan mendapatkan layanan konsultasi sesuai antrian;<br />
                        Pemberian konsultasi dilakukan di ruang layanan;<br />
                        Pengguna dapat melakukan konsultasi melalui sambungan telepon atau surat elektronik;<br />
                        Petugas mencatat data setiap pengguna yang meminta layanan informasi.<br />


                        #3 Layanan penyediaan dokumen standar<br />

                        Mekanisme dan prosedur untuk layanan ini adalah sebagai berikut:<br />

                        Pengguna datang langsung ke LITe BSN atau melalui website Pemesanan Standar Online (Pesta Online) di alamat pesta.bsn.go.id;<br />
                        Pengguna mengakses langsung aplikasi Pemesanan Standar Online dan memilih dokumen SNI yang diperlukan atau mengisi form apabila menginginkan reproduksi/pembelian asli standar internasional (non-SNI);<br />
                        Daftar standar yang dipesan akan tercantum dalam rincian biaya;<br />
                        Rincian pemesanan standar non-SNI hanya berlaku 2 minggu, apabila melewati batas waktu tersebut, pemesanan dibatalkan dan harus melakukan pemesanan ulang;<br />
                        Jika pengguna setuju dengan rincian biaya dalam pemesanan sesuai PP No. 40 Tahun 2018 tentang PNBP BSN, pengguna melakukan konfirmasi persetujuan pembayaran sekaligus permohonan kode biling;<br />
                        Petugas layanan menerbitkan kode billing berupa 15 digit angka sebagai dasar pembayaran oleh pengguna melalui saluran yang tersedia;<br />
                        Setelah pembayaran diverifikasi, petugas layanan memproses dokumen standar sesuai permintaan;<br />
                        Dokumen standar hasil proses reproduksi baik tercetak maupun elektronik (SNI dan/atau reproduksi/pembelian asli standar non-SNI) diserahkan kepada pembeli secara langsung, melalui jasa pengiriman dokumen atau melalui e-mail untuk pembelian SNI dalam bentuk elektronik.<br />


                        4. Jangka waktu penyelesaian layanan<br />

                        Perpustakaan BSN berkomitmen untuk melaksanakan Layanan Informasi SPK sesuai dengan jangka waktu penyelesaian layanan sebagaiman berikut:<br />

                        Permintaan cetakan dokumen standar (untuk satu eksemplar standar) oleh pengguna yang datang langsung adalah 35 menit setelah bukti pembayaran diterima dan diverifikasi;<br />
                        Setiap penambahan eksemplar standar yang diminta oleh pengguna yang sama (pada saat yang sama) adalah 15 menit untuk setiap eksemplar;<br />
                        Permintaan SNI dalam format elektronik (PDF) akan dikirimkan dalam jangka waktu 1x24 jam hari kerja ke alamat e-mail pengguna setelah bukti pembayaran diterima dan diverifikasi petugas. Apabila terpotong hari libur, maka dokumen akan dikirimkan di hari kerja berikutnya;<br />
                        Jangka waktu pengiriman dokumen standar tercetak menyesuaikan dengan jasa pengiriman yang digunakan;<br />
                        Respon atau jawaban permintaan informasi yang ditujukan kepada layanan informasi melalui surat (baik surat biasa maupun elektronik) paling lambat 5 hari kerja;<br />
                        Jangka waktu peminjaman buku referensi dan dokumen SNI oleh pengguna internal BSN adalah 10 hari kerja;<br />
                        Layanan konsultasi di tempat dilakukan sesuai dengan nomor antrian pada jam layanan.<br />
                        Antrian konsultasi dan layanan dokumen standar terakhir diterima maksimal 30 menit sebelum jam layanan tutup.<br />


                        5. Biaya/tarif layanan<br />

                        Biaya (tarif) layanan pada Layanan Informasi SPK di LITe BSN hanya diperuntukkan untuk layanan dokumen standar, baik SNI maupun standar internasional (non-SNI). Layanan konsultasi dan perpustakaan tidak dikenakan biaya. Ketentuan mengenai biaya (tarif) layanan dokumen standar adalah sebagai berikut:<br />

                        Tarif layanan dokumen standar ditetapkan sesuai dengan Peraturan Pemerintah Nomor 40 Tahun 2018 tentang Jenis dan atas Jenis PNBP yang Berlaku pada BSN.<br />
                        Tarif sebagaimana tercantum pada PP No. 40 Tahun 2018 tidak termasuk biaya pengiriman dokumen, transfer pembayaran dan/atau royalti. Biaya-biaya tersebut dibebankan kepada pengguna dalam hal; (a) biaya pengiriman dokumen bagi yang meminta dokumen dikirimkan; besaran biaya menyesuaikan dengan daftar biaya pada jasa pengiriman yang digunakan; (b) biaya transfer pembayaran untuk pembayaran dokumen kepada badan standar asing di luar negeri; besaran biaya menyesuaikan dengan ketentuan pihak terkait (bank, ekspedisi pengiriman, dan badan standar) dan/atau (c) biaya royalti yang dipersyaratkan oleh badan standar asing atas publikasi standar yang diadopsi menjadi SNI; dilaksanakan sesuai nota kesepakatan atau perjanjian antara BSN dengan badan standar asing.<br />
                        Bagi pembeli dari luar wilayah NKRI, tarif di konversi ke dalam mata uang Dollar Amerika (USD) dengan menggunakan kurs jual pada saat transaksi.</p>
